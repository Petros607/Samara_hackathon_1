class SummaryLection:
    _book_combine_prompt = PromptTemplate(
        template="""Суммаризируй части лекции в один подробный текст на 30-40 предложений. 


        "{text}"


        Теперь суммаризируй части лекции в один подробный текст на 30-40 предложений.
        Краткое содержимое:"""
    )
    _book_map_prompt = PromptTemplate(
        template="""Напиши краткое, но подробное содержимое части лекции,
        не забывая важные моменты и точные данные 
                                    
                                    
        "{text}"


        Теперь напиши краткое, но подробное содержимое части лекции,
        не забывая важные моменты и точные данные:
        """
    )

    def __init__(self, model: str = "GigaChat"):
        self.giga = GigaChat(
            credentials=os.environ.get("giga_token"),
            model=model,
            verify_ssl_certs=False,
        )

    @staticmethod
    def transform_json_to_txt(input_file: str, output_file: str):
        """Трансформирует json на выходе whisper в текст для парсинга GigaChat

        Args:
            input_file (str): путь к файлу с результатом распознавания (НЕ РАЗБИТЫЙ ПО СЛАЙДАМ И ВРЕМЕНИ)
            output_file (str): куда временно сохранить текст для GigaChat
        """
        with open(input_file, "r", encoding="utf-8") as f:
            text_parts = json.load(f)
        text = "".join([part["text"] for part in text_parts])
        with open(output_file, "w", encoding="utf-8") as f:
            f.write(text)

    def summarize_from_file(self, input_file: str) -> str:
        loader = TextLoader(input_file, encoding="utf-8")
        documents = loader.load()
        text_splitter = RecursiveCharacterTextSplitter(
            chunk_size=7000,
            chunk_overlap=0,
            length_function=len,
            is_separator_regex=False,
        )
        documents = text_splitter.split_documents(documents)

        chain = load_summarize_chain(
            self.giga,
            chain_type="map_reduce",
            map_prompt=self._book_map_prompt,
            combine_prompt=self._book_combine_prompt,
            verbose=False,
        )
        res = chain.invoke({"input_documents": documents})
        return res["output_text"].replace(". ", ".\n")


